namespace TiledCS;

/// <summary>
/// Represents a tile animation. Tile animations are a group of tiles which act as frames for an animation.
/// </summary>
public class TiledTileAnimation
{
    /// <summary>
    /// The tile id within a tileset
    /// </summary>
    public int Tileid;
    /// <summary>
    /// The duration in miliseconds
    /// </summary>
    public int Duration;
}